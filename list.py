fruits = ["apple","banana","mango","watermelon","grapes"]
vegetable = ["potato","tomato","onion"]
print(fruits)

# access item
print(fruits[2])

# replace item
fruits[1] = "orange"
print(fruits)

# add item
fruits.append("cherry")
print(fruits)

# remove item
fruits.remove("grapes")
print(fruits)

# sorts item
fruits.sort
print(fruits)

# copy item
fruits.copy()
print(fruits)

# add list
add = fruits + vegetable
print(add)